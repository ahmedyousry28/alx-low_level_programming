#include <stdio.h>

/**
 * main - Prints the program name followed by a new line
 *
 * @argc: The number of arguments of the program
 *
 * @argv: this is array of pointers to arguments
 *
 *
 * Return: Always (0)
 */


int main(int __attribute__((__unused__)) argc, char *argv[])
{
	printf("%s\n", argv[0]);

	return (0);
}
